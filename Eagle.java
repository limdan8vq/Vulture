public class Eagle {
    
    public int x;
    public int y;
    
    public Eagle(int xc, int yc) {
        x = xc;
        y = yc;
    }
    
    public void setX(int xc) {
        x = xc;
    }
    
    public void setY(int yc) {
        y = yc;
    }
    
    public int getX() {
        return x;
    }
    
    public int getY() {
        return y;
    }    
}
